public class Switch {
    public static void main(String[] args) {
        int x = 3;

        switch (x)  {
            case 1:
            System.out.println("El valor no es 1");break;
            case 3:
            System.out.println("El valor es 3");break;        
            default:
            System.out.println("el valor no es ningun");
        }
    }
}
